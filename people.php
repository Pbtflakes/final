          <dd>
          Both colleague and competitor of Gropius, Mies would begin his career
          in the modernist style outside of the Bauhaus.  His directorship
          began in 1930 at Gropius' insistence, but could not protect the
          faltering school from rising Nazi opposition to modernist art.
          <br>
          He saw much success after emigrating to the United States, spending
          the rest of his career based at his Chicago studio.
          </dd>
