          <dt>Walter Gropius</dt>
          <img class=portrait
               src="img/portrait/gropius.jpg"
               alt="Walter Gropius, resting his head on his left hand">
          <dd>
          <p>
          Walter Gropius was the founder of the Bauhaus in 1919.
          His goal was "to create a new guild of craftsmen, without
          the class distinctions which raise an arrogant barrier
          between craftsman and artist." He remained involved with
          the Bauhaus throughout its life, and bounced through
          various artists' circles after its closure.
          </p>
          <p>
          As with many other members of the school, he settled
          outside of Europe after its demise.
          </p>
          </dd>
          <dt>Marcel Breuer</dt>
          <img class=portrait
               src="img/portrait/breuer.png"
               alt="Marcel Breuer in suspenders, gesturing to person behind
                     camera with open hands">
          <dd>
          Head of the Bauhaus' carpentry department, Marcel Breuer
          was the creator of many famous pieces of modernist
          furniture.  His career in architecture after the Bauhaus,
          centered in New York, would further cement his legacy.
          Works include IBM La Gaude, the Met Breuer, the headquarters
          of UNESCO, and many more buildings which extended modernism
          into the brutalist style.
          </dd>
          <dt>Lucia Moholy</dt>
          <img class=portrait
               src="img/portrait/moholy.jpg"
               alt="Lucia Moholy in self-portrait, chin on heels of hands">
          <dd>
          <p>
          Lucia Moholy was the serially undercredited photographer
          and editor who documented the Bauhaus.  She worked as
          part of the <span lang=de>Neue Sachlichkeit</span>, or
          New Objectivity, movement as she cataloged the school's
          facilities and projects.  This distinctive style's
          documentary focus on the truth can be seen in her frank
          self-portrait.
          </p>
          <p>
          She was forced to leave behind the bulky glass negatives
          of her photos when she fled Berlin upon the rise of the
          Nazi Party, and these original copies of her work were
          subsequently held by Walter Gropius.  She was able to
          recover most by the 1960s, but only after struggling to
          have her photographs recognized as her own in Bauhaus
          publications.
          </p>
          </dd>
          <dt>Margaretha Reichardt</dt>
          <img class=portrait
               src="img/portrait/grete.jpg"
               alt="Grete Reichardt, looking right towards camera">
          <dd>
          Margaretha Reichardt, also known as Grete, was a weaver
          and textile artist from Erfurt, Germany.  A member of the
          Bauhaus from 1926 to 1931, she was one of the most important
          members of the weaving workshop.  From commercially-produced
          toys to airplane seats, her works embodied the importance
          the school placed on the crafts, as well as practicality
          in all they produced.
