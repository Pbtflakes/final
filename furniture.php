        <p>
        Marcel Breuer, taken into the Bauhaus after heading out
        on his own at age 18, was quickly placed at the lead of
        their carpentry shop.  His designs pioneered the use of
        bent steel tubing as the sole structural element of chairs,
        and are produced under license to this day.
        </p>
        <p>
        One such design, named the Model B3, combines this tubing
        with straps of <span lang=de>Eisengarn</span>, or "iron yarn"
        in English.  A durable, smooth cloth used for all supporting
        surfaces of the chair, it is woven out of cotton thread
        coated in parrafin wax.  This was helped along by textile
        artist Margaretha Reichardt, who worked towards improving
        the quality of <span lang=de>Eisengarn</span>.
        </p>
